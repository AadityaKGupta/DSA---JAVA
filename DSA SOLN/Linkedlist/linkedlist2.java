//Que 9:- Given head , the head of a linked list, determine if the linked list has a cycle in it.
package Linkedlist;
public class linkedlist2 {
    public static void main(String[] args) {
        ListNode head = new ListNode(0);
        head.next = new ListNode(1);
        head.next.next = new ListNode(2);
        head.next.next.next = new ListNode(3);
        head.next.next.next.next = new ListNode(4);
        head.next.next.next.next.next = head.next;

        System.out.println(detectlifecycle(head));  //true..
    }

    public static boolean detectlifecycle(ListNode head){
        ListNode slow = head;
        ListNode fast = head;

        while(fast!=null && fast.next!=null){
            slow = slow.next;
            fast = fast.next.next;

            if(slow == fast){
                return true;
            }
        }
        return false;
    }
}
